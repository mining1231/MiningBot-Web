const successRate = document.getElementById("successRate");
const failRate = document.getElementById("failRate");
const expValue = document.getElementById("expValue");
const dropList = document.getElementById("dropList");
const levelTitle = document.querySelector(".farm-level-title");

function getIcon(icon) {

    if (icon === "shine") {
        return '<img src="../../images/shine.png" alt="샤인머스켓">';
    }

    if (icon === "sansam") {
        return '<img src="../../images/sansam.png" alt="산삼">';
    }

    return `<span>${icon}</span>`;
}

function loadLevel(level) {

    const data = farmingData[level];

    levelTitle.textContent = `🌱 Lv.${level}`;

    successRate.textContent = data.success;
    failRate.textContent = data.fail;
    expValue.textContent = `${data.exp} EXP`;

    dropList.innerHTML = "";

    data.drops.forEach(drop => {

        dropList.innerHTML += `
            <div class="drop-row">

                <div class="drop-left">
                    ${getIcon(drop.icon)}
                    <span>${drop.name}</span>
                </div>

                <div class="drop-right">
                    ${drop.chance}
                </div>

            </div>
        `;

    });

}
const buttons = document.querySelectorAll(".level-btn");

buttons.forEach(button => {

    button.addEventListener("click", () => {

        buttons.forEach(btn => btn.classList.remove("active"));

        button.classList.add("active");

        loadLevel(button.dataset.level);

    });

});

buttons[0].classList.add("active");

loadLevel(1);