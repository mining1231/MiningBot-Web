const levelSelect = document.getElementById("upgradeLevel");
const rangeButtons = document.querySelectorAll(".upgrade-range-btn");

const levelLabel = document.getElementById("upgradeLevelLabel");
const weaponName = document.getElementById("upgradeWeaponName");
const successValue = document.getElementById("upgradeSuccess");
const failValue = document.getElementById("upgradeFail");
const destroyValue = document.getElementById("upgradeDestroy");
const costValue = document.getElementById("upgradeCost");
const hiddenNotice = document.getElementById("upgradeHiddenNotice");

function createLevelOptions(start, end) {
    levelSelect.innerHTML = "";

    for (let level = start; level <= end; level++) {
        const option = document.createElement("option");

        option.value = level;
        option.textContent = `${level}강 - ${upgradeData[level].name}`;

        levelSelect.appendChild(option);
    }

    loadUpgradeLevel(start);
}

function loadUpgradeLevel(level) {
    const data = upgradeData[level];

    if (!data) return;

    levelSelect.value = String(level);

    levelLabel.textContent = `Lv.${level}`;
    weaponName.textContent = data.name;

    successValue.textContent = data.success;
    failValue.textContent = data.fail;
    destroyValue.textContent = data.destroy;
    costValue.textContent = data.cost;

    hiddenNotice.classList.toggle("show", Boolean(data.hidden));
}

rangeButtons.forEach(button => {
    button.addEventListener("click", () => {
        rangeButtons.forEach(btn => btn.classList.remove("active"));

        button.classList.add("active");

        const start = Number(button.dataset.start);
        const end = Number(button.dataset.end);

        createLevelOptions(start, end);
    });
});

levelSelect.addEventListener("change", () => {
    loadUpgradeLevel(Number(levelSelect.value));
});

createLevelOptions(1, 10);